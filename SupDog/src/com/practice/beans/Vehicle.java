package com.practice.beans;

public abstract class Vehicle {
	
	private int fuel;
	private int speed;
	
	public int getFuel() {
		return fuel;
	}
	public void setFuel(int fuel) {
		this.fuel = fuel;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public abstract void idk();
	
	
}
